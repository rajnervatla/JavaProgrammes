package java;

public class ConvertingStringtoInt {
	public static void main(String[] args) 
	{
	// Create a string literal   
	   String text = "50";  
	// Convert string into int using Integer.parseInt() method. 
	   int num = Integer.parseInt(text);  
	// Print value of num.  
	   System.out.println(num);
	 }

}
